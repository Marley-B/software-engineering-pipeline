
def add(a, b):
    return a + b
def sub(a, b):
    return a - b
def factorial(a): 
    if a <= 1: return 1
    return factorial(a - 1) * a
def divide(a,b):
    return a/b

def multiply(a, b):
    return a * b
def root(a, b):
    if(b == 0):
        return None
    return a **(1/b)
def square(a,b):
    return a ** b
